def system_language(locale_name: str | None = None) -> str:
    """Return the supported language matching an OS locale."""
    candidate = locale_name
    if candidate is None:
        try:
            candidate = locale.getlocale()[0]
        except (TypeError, ValueError):
            candidate = None
    return "ko" if str(candidate or "").lower().startswith("ko") else "en"


class Translator:
    """Translate stable UI keys while making missing keys obvious in tests."""
    def __init__(self, language: str | None = None) -> None:
        self.language = normalize_language(language)
    def text(self, key: str, **values: object) -> str:
        catalog = TRANSLATIONS[self.language]
        template = catalog.get(key, TRANSLATIONS["en"].get(key, key))
        return template.format(**values) if values else template
    def mapping(self) -> Mapping[str, str]:
        return TRANSLATIONS[self.language]


def normalize_language(language: str | None) -> str:
    if language is None:
        return system_language()
    normalized = language.lower().split("_", 1)[0].split("-", 1)[0]
    if normalized not in SUPPORTED_LANGUAGES:
        raise ValueError(f"Unsupported language: {language}. Choose ko or en.")
    return normalized


def missing_translation_keys() -> dict[str, set[str]]:
    all_keys = set().union(*(catalog.keys() for catalog in TRANSLATIONS.values()))
    return {language: all_keys - set(catalog) for language, catalog in TRANSLATIONS.items()}


